<p style="text-align: justify;"><span style="font-weight: 400;">It was Pierre-Paul Riquet who, in the 17th century, built the Canal du Midi, formerly known as the Royal Canal of Languedoc. While the canal has inspired many other works, it was itself inspired by canals built since antiquity. </span></p>
<p style="text-align: justify;"><img class="imageRight" src="https://upload.wikimedia.org/wikipedia/commons/thumb/3/3c/Pierre-Paul_Riquet_%281604-1680%29.jpg/568px-Pierre-Paul_Riquet_%281604-1680%29.jpg" alt="Pierre-Paul Riquet" width="216" height="228" style="float: right;" /></p>
<p style="text-align: justify;"><span style="font-weight: 400;">&nbsp;&nbsp;</span></p>
<p style="text-align: justify;">The construction of a waterway linking the Atlantic Ocean and the Mediterranean Sea represents an economic and political challenge, but it is not a new idea, as many previous projects have encountered difficulties, largely due to water supply concerns and topography considered too complicated to manage. In fact, the canal allowed the transport of goods and, more generally, boats, to bypass the Iberian Peninsula and thus avoid the Spanish tax when passing through the Strait of Gibraltar, while ensuring a direct and safe route, bypassing more or less practicable land routes or bad encounters.</p>
<p style="text-align: justify;">While the region of Languedoc, up to that of Toulouse, shows hostility towards the royal power. The canal made it possible to deprive Spain of part of its resources while developing trade in the South.</p>
<p style="text-align: justify;">Thus, Louis XIV considered positively the canal project of Pierre-Paul Riquet, which he discovered in 1662. In 1664, the expertise of the project by the States of Languedoc confirms its feasibility, and is officially validated by the king. Work began in 1666. The canal was inaugurated on 15 May 1681 and finally opened to traffic in July 1684 along its entire route. Thus it straddles three departments: the Haute-Garonne, the Aude and the H&eacute;rault. Between Marseillan, where it leads to the Etang de Thau, and Toulouse, it covers 241 km.</p>
